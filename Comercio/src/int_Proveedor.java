    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try{
            Connection cn = DriverManager.getConnection(mdi_Principal.BD, mdi_Principal.Usuario, mdi_Principal.Contraseña);
            PreparedStatement pst = cn.prepareStatement("select * from proveedor where nit_proveedor =?");
            pst.setString(1, txtBuscado.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                txtNIT.setText(rs.getString("nit_proveedor"));
                txtNIT.setEnabled(false);
                txtNombre.setText(rs.getString("nombre_proveedor"));
                txtDireccion.setText(rs.getString("direccion"));
                txtBuscado.setText("");

                
                
                btnModificar.setEnabled(true);
                btnEliminar.setEnabled(true);
                btnIngresar.setEnabled(false);
                

            } else {
                JOptionPane.showMessageDialog(null, "Proveedor no registrado.");
            }

        }catch (Exception e){

        }
        //bitacora_busqueda();
    }//GEN-LAST:event_btnBuscarActionPerformed

